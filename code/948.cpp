class Solution {
public:
    int bagOfTokensScore(vector<int>& tokens, int power) {
        sort(tokens.begin(), tokens.end());
        int res = 0, pts = 0, i = 0, j = tokens.size() - 1;
        while(i <= j) {
            if(power >= tokens[i]) {
                power -= tokens[i++];
                res = max(res, ++pts);
            } else if(pts > 0) {
                power += tokens[j--];
                pts--;
            } else break;
        }
        
        return res;
    }
};