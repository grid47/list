class Solution {
public:
    int maxEqualRowsAfterFlips(vector<vector<int>>& mtx) {
        unordered_map<string, int> mp;
        for(auto x: mtx) {
            string s = "";
            int top = x[0];
            for(int i = 0; i < x.size(); i++) {
                if(x[i] == top) s += '1';
                else s += '0';
            }
            mp[s]++;
        }
        int res = 0;
        for(auto [k, v]: mp)
            res = max(res, v);
        return res;
    }
};