class Solution {
public:
    set<int> cnt;
    
    void fact(int num) {
        while(num % 2 == 0) {
            cnt.insert(2);
            num /= 2;
        }
        for(int i = 3; i <= num; i+=2) {
            while(num % i == 0) {
                cnt.insert(i);
                num /= i;
            }
        }
    }
    
    int distinctPrimeFactors(vector<int>& nums) {
        
        int n = nums.size();
        for(int i = 0; i < n; i++)
            fact(nums[i]);
        
        
        return cnt.size() ;
    }
};